function traducirAMorse() {
    const textoInput = document.getElementById('textoInput').value.toUpperCase();
    const resultadoElement = document.getElementById('resultado');
    
    const morseCode = textoInput.split('').map(char => {
        return (char === ' ') ? ' ' : `${char}: ${MorseCodeMap[char]}`;
    }).join('<br>');

    resultadoElement.innerHTML = `Texto en Morse:<br>${morseCode}`;
}

